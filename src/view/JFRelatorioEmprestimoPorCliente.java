    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        limparDadosEmprestimos();
        
        try {
            List<Cliente> clientes = getClientes();
            jComboBox1.setRenderer(new DefaultListCellRenderer() {
                @Override
                public Component getListCellRendererComponent(JList list, Object value, int index, boolean isSelected, boolean cellHasFocus) {
                    super.getListCellRendererComponent(list, value, index, isSelected, cellHasFocus);
                    if (value instanceof Cliente) {
                        Cliente person = (Cliente) value;
                        setText(person.getNome());
                    }
                    return this;
                }
            });
            jComboBox1.setModel(new DefaultComboBoxModel(clientes.toArray()));
            jComboBox1.setSelectedIndex(-1);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(rootPane, "Ocorreu um problema para listar os clientes");
        }

    }//GEN-LAST:event_formWindowOpened

