    private List<Cliente> getClientes() throws SQLException {
        BdCliente d = new BdCliente();
        return d.getLista();
    }
    
    private void onCustomerComboboxItemClicked() {
        if (jComboBox1.getSelectedItem() != null) {
            limparDadosEmprestimos();
            
            try {
                Cliente cliente = (Cliente) jComboBox1.getSelectedItem();
                int idCliente = cliente.getId();
                
                BdEmprestimo bd = new BdEmprestimo();
                List<RelatorioEmprestimo> emprestimos = bd.getRelatorioEmprestimosPorCliente(idCliente);
                
                int totalLivros = 0;
                int totalEmprestado = 0;
                
                DefaultTableModel model = (DefaultTableModel) tblEmprestimos.getModel();
                for (RelatorioEmprestimo emprestimo : emprestimos) {
                    model.addRow(new Object[]{emprestimo.getId(), emprestimo.getLivro(), emprestimo.getData_emprestimo(), emprestimo.isDevolvido()});
                    totalLivros++;
                    
                    if (!emprestimo.isDevolvido()) {
                        totalEmprestado++;
                    }
                }
                
                lblTotalEmprestados.setText(String.valueOf(totalEmprestado));
                lblTotalLivros.setText(String.valueOf(totalLivros));
            } catch (SQLException e) {
                System.out.println(e.getMessage());
                JOptionPane.showMessageDialog(rootPane, "Ocorreu um problema para listar os empréstimos do cliente");
            }
        }
    }
    
    private void limparDadosEmprestimos() {
        DefaultTableModel model = (DefaultTableModel) tblEmprestimos.getModel();
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        
        lblTotalEmprestados.setText("0");
        lblTotalLivros.setText("0");
    }

